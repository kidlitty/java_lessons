public class VarKeywordExample {
	public static void main(String[] args){

		// int
		var x = 20;

		// double
		var y = 10.5;

		// char
		var z = 'b';

		// string
		var p = "The brown fox";

		// boolean
		var q = true;


		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(p);
		System.out.println(q);
	}
}
